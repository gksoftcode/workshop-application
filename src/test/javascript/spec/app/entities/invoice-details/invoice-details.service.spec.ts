/* tslint:disable max-line-length */
import axios from 'axios';
import sinon from 'sinon';

import InvoiceDetailsService from '@/entities/invoice-details/invoice-details.service';
import { InvoiceDetails } from '@/shared/model/invoice-details.model';
import { DiscountType } from '@/shared/model/enumerations/discount-type.model';

const error = {
  response: {
    status: null,
    data: {
      type: null,
    },
  },
};

const axiosStub = {
  get: sinon.stub(axios, 'get'),
  post: sinon.stub(axios, 'post'),
  put: sinon.stub(axios, 'put'),
  patch: sinon.stub(axios, 'patch'),
  delete: sinon.stub(axios, 'delete'),
};

describe('Service Tests', () => {
  describe('InvoiceDetails Service', () => {
    let service: InvoiceDetailsService;
    let elemDefault;

    beforeEach(() => {
      service = new InvoiceDetailsService();
      elemDefault = new InvoiceDetails(123, 0, 'AAAAAAA', 0, 0, DiscountType.PERCENTAGE, 0, 0);
    });

    describe('Service methods', () => {
      it('should find an element', async () => {
        const returnedFromService = Object.assign({}, elemDefault);
        axiosStub.get.resolves({ data: returnedFromService });

        return service.find(123).then(res => {
          expect(res).toMatchObject(elemDefault);
        });
      });

      it('should not find an element', async () => {
        axiosStub.get.rejects(error);
        return service
          .find(123)
          .then()
          .catch(err => {
            expect(err).toMatchObject(error);
          });
      });

      it('should create a InvoiceDetails', async () => {
        const returnedFromService = Object.assign(
          {
            id: 123,
          },
          elemDefault
        );
        const expected = Object.assign({}, returnedFromService);

        axiosStub.post.resolves({ data: returnedFromService });
        return service.create({}).then(res => {
          expect(res).toMatchObject(expected);
        });
      });

      it('should not create a InvoiceDetails', async () => {
        axiosStub.post.rejects(error);

        return service
          .create({})
          .then()
          .catch(err => {
            expect(err).toMatchObject(error);
          });
      });

      it('should update a InvoiceDetails', async () => {
        const returnedFromService = Object.assign(
          {
            itemNo: 1,
            itemDesc: 'BBBBBB',
            itemPrice: 1,
            itemQty: 1,
            discountType: 'BBBBBB',
            discount: 1,
            totalAmount: 1,
          },
          elemDefault
        );

        const expected = Object.assign({}, returnedFromService);
        axiosStub.put.resolves({ data: returnedFromService });

        return service.update(expected).then(res => {
          expect(res).toMatchObject(expected);
        });
      });

      it('should not update a InvoiceDetails', async () => {
        axiosStub.put.rejects(error);

        return service
          .update({})
          .then()
          .catch(err => {
            expect(err).toMatchObject(error);
          });
      });

      it('should partial update a InvoiceDetails', async () => {
        const patchObject = Object.assign(
          {
            itemDesc: 'BBBBBB',
            itemQty: 1,
            totalAmount: 1,
          },
          new InvoiceDetails()
        );
        const returnedFromService = Object.assign(patchObject, elemDefault);

        const expected = Object.assign({}, returnedFromService);
        axiosStub.patch.resolves({ data: returnedFromService });

        return service.partialUpdate(patchObject).then(res => {
          expect(res).toMatchObject(expected);
        });
      });

      it('should not partial update a InvoiceDetails', async () => {
        axiosStub.patch.rejects(error);

        return service
          .partialUpdate({})
          .then()
          .catch(err => {
            expect(err).toMatchObject(error);
          });
      });

      it('should return a list of InvoiceDetails', async () => {
        const returnedFromService = Object.assign(
          {
            itemNo: 1,
            itemDesc: 'BBBBBB',
            itemPrice: 1,
            itemQty: 1,
            discountType: 'BBBBBB',
            discount: 1,
            totalAmount: 1,
          },
          elemDefault
        );
        const expected = Object.assign({}, returnedFromService);
        axiosStub.get.resolves([returnedFromService]);
        return service.retrieve({ sort: {}, page: 0, size: 10 }).then(res => {
          expect(res).toContainEqual(expected);
        });
      });

      it('should not return a list of InvoiceDetails', async () => {
        axiosStub.get.rejects(error);

        return service
          .retrieve()
          .then()
          .catch(err => {
            expect(err).toMatchObject(error);
          });
      });

      it('should delete a InvoiceDetails', async () => {
        axiosStub.delete.resolves({ ok: true });
        return service.delete(123).then(res => {
          expect(res.ok).toBeTruthy();
        });
      });

      it('should not delete a InvoiceDetails', async () => {
        axiosStub.delete.rejects(error);

        return service
          .delete(123)
          .then()
          .catch(err => {
            expect(err).toMatchObject(error);
          });
      });
    });
  });
});
