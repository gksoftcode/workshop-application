import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IPurchaseOrderDetails } from '@/shared/model/purchase-order-details.model';

const baseApiUrl = 'api/purchase-order-details';

export default class PurchaseOrderDetailsService {
  public find(id: number): Promise<IPurchaseOrderDetails> {
    return new Promise<IPurchaseOrderDetails>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IPurchaseOrderDetails): Promise<IPurchaseOrderDetails> {
    return new Promise<IPurchaseOrderDetails>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IPurchaseOrderDetails): Promise<IPurchaseOrderDetails> {
    return new Promise<IPurchaseOrderDetails>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IPurchaseOrderDetails): Promise<IPurchaseOrderDetails> {
    return new Promise<IPurchaseOrderDetails>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
